int	ft_atoi(const char *str)
{
	int		n;
	char	s;

	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	s = *str;
	if (s == '-' || s == '+')
		str++;
	n = 0;
	while (*str >= '0' && *str <= '9')
		n = n * 10 + *(str++) - '0';
	return (s == '-' ? -n : n);
}
